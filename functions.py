def area(width, height):
    result = width * height
    return result

num_1 = area(5, 6)
print(num_1)
num_2 = area(4, 12)
print(num_2)
num_3 = area(5, 8)
print(num_3)

def subtract(num1, num2):
    result = num1 - num2
    return result

num_4 = subtract(3, 2)
print(num_4)
num_5 = subtract(6, 3)
